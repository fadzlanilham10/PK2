    private void tabel() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Nama Perusahaan");
        tbl.addColumn("Alamat");
        tbl.addColumn("No Hp");
        tbl.addColumn("Email");
        tbl.addColumn("Pengembang");
        tbl.addColumn("Tipe Perumahan");
        tbl.addColumn("File Brosur");
        tabel.setModel(tbl);
        try {
            Statement statement = (Statement) Koneksi.sambungDB().createStatement();
            ResultSet res = statement.executeQuery("Select * from tb_perumahan");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("nama_perumahan"),
                    res.getString("alamat"),
                    res.getString("no_hp"),
                    res.getString("email"),
                    res.getString("pengembang"),
                    res.getString("tipe_perumahan"),
                    res.getString("file_brosur"),});
                tabel.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

