    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        String nama = txt_nama.getText();
        String alamat = txt_alamat.getText();
        String no_HP = txt_noHP.getText();
        String email = txt_email.getText();
        String pengembang = txt_pengembang.getText();
        String tipe = (String) cmb_tipe.getSelectedItem();
        String filebrosur = txt_file.getText();
        try {
            Statement statement = (Statement) Koneksi.sambungDB().createStatement();
            statement.executeUpdate("insert into tb_perumahan VALUES ('" + nama + "','" + alamat + "','" + no_HP + "','" + email + "','" + pengembang + "','" + tipe + "','" + filebrosur + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
        tabel();
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if (btn_edit.getText() == "btn_edit") {
            txt_nama.requestFocus();
        } else {
            String nama = txt_nama.getText();
            String alamat = txt_alamat.getText();
            String no_Hp = txt_noHP.getText();
            String email = txt_email.getText();
            String pengembang = txt_pengembang.getText();
            String tipe = (String) cmb_tipe.getSelectedItem();
            String file_brosur = txt_file.getText();
            try {
                Statement statement = (Statement) Koneksi.sambungDB().createStatement();
                statement.executeUpdate("update tb_perumahan SET nama_perumahan='" + nama + "'," + "alamat='" + alamat + "'," + "no_telp='" + no_Hp + "'," + "email='" + email + "'," + "pengembang='" + pengembang + "'," + "tipe_perumahan='" + tipe + "'," + "file_brosur='" + file_brosur + "'" + "WHERE nama_perumahan = '" + nama + "' OR alamat = '" + alamat + "'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Gagal diubah");
            }
            tabel();
        }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        String nama = txt_nama.getText();
        String alamat = txt_alamat.getText();
        int baris = tabel.getSelectedRow();
        if (baris != -1) {
            int pilihan = JOptionPane.showConfirmDialog(this, "Apakah anda yakin?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (pilihan == 0) {
                try {
                    Statement statement = (Statement) Koneksi.sambungDB().createStatement();
                    statement.executeUpdate("delete from tb_perumahan where nama_perumahan = '" + nama + "' OR alamat ='" + alamat + "';");
                    JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");
                    txt_nama.setText("");
                    txt_alamat.setText("");
                    txt_noHP.setText("");
                    txt_email.setText("");
                    txt_pengembang.setText("");
                    cmb_tipe.setSelectedItem("A");
                    txt_alamat.setText("");
                    txt_file.setText("");
                    txt_nama.requestFocus();
                } catch (Exception t) {
                    JOptionPane.showMessageDialog(null, "Data Gagal di Hapus");
                }
                tabel();
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        try {
            Statement statement = (Statement) Koneksi.sambungDB().createStatement();
            ResultSet res = statement.executeQuery("select*from tb_perumahan where " + "nama_perumahan like '%" + txt_cari.getText() + "%' OR " + "alamat like '%" + txt_cari.getText() + "%'");
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("Nama Perumahan");
            tbl.addColumn("Alamat");
            tbl.addColumn("No Hp");
            tbl.addColumn("Email");
            tbl.addColumn("Pengembang");
            tbl.addColumn("Tipe Perumahan");
            tbl.addColumn("File Brosur");

            tabel.setModel(tbl);
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("nama_perumahan"),
                    res.getString("alamat"),
                    res.getString("no_hp"),
                    res.getString("email"),
                    res.getString("pengembang"),
                    res.getString("tipe_perumahan"),
                    res.getString("file_brosur"),});
                tabel.setModel(tbl);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }//GEN-LAST:event_btn_cariActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int i = tabel.getSelectedRow();
        if (i == -1) {
            return;
        }
        String nama = (String) tabel.getValueAt(i, 0);
        txt_nama.setText(nama);
        String alamat = (String) tabel.getValueAt(i, 1);
        txt_alamat.setText(alamat);
        String no = (String) tabel.getValueAt(i, 2);
        txt_noHP.setText(no);
        String email = (String) tabel.getValueAt(i, 3);
        txt_email.setText(email);
        String pengembang = (String) tabel.getValueAt(i, 4);
        txt_pengembang.setText(pengembang);
        String tipe = (String) tabel.getValueAt(i, 5);
        cmb_tipe.setSelectedItem(tipe);
        String filebrosur = (String) tabel.getValueAt(i, 6);
        txt_file.setText(filebrosur);
    }//GEN-LAST:event_tabelMouseClicked

